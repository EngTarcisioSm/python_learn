
# Função print
print(123456)

# Pode receber mais de um atributo separados por virgula
print("Tarcísio", "dev")

# Alterando o separador padrão do python com o atributo nomeado sep='novoSeparador'
print('Tarcísio', 'dev', 32, sep='-')  # substitui o separador espaço por um hífen

# Alterar o indicador de final de linha do comando print
print("Tarcísio", end='\t')  # substituindo a quebra de linha ao final por \t (tabulação)
print('Bryan')

# Python é case sensitive


