'''
Operadores matemáticos
+   soma
-   subtração
*   multiplicação
/   divisão
//  divisão inteira
**  exponenciação
%   resto de divisão
()  parentesis - altera a precedencia de operações
'''
print('Nultiplicação', 10 * 10)
print('Soma', 10 + 10)
print('Subtração', 10 -5)
print('Divisão', 10 / 2)

# multiplicação apresenta comportamento diferente quando utilizado com strings
print('Repetição: ', 3 * 'String')

# operador soma gera um resultado de concatenação quanto utilizado com strings
print('Concatenação: ', 'a' + 'A')

# divisão inteira
print(10.5 // 3)

# exponenciação
print(2**3)

# resto de divisão
print(10 % 3)